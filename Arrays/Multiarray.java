public class Multiarray {
    public static void main(String args[]) {
        int[][] matrix = { { 2, 3 }, { 4, 5 }, { 8, 9 } };
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 2; j++) {
                System.out.println(matrix[i][j]);
            }

        }
    }

}
